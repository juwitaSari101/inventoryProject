    private boolean validasiInput() {
        boolean valid = true;
        if (t_tanggalMasuk.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "tanggal tidak boleh kosong");
            valid = false;
        } else if (t_kodeBarangMasuk.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kode barang masuk tidak boleh kosong");
            valid = false;
        } else if (t_namaBarangMasuk.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih Barang Masuk");
            valid = false;
        } else if (t_jumlah.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Jumlah Barang tidak boleh kosong");
            valid = false;
        } else {
            valid = true; // Pastikan valid true jika semua validasi terpenuhi
        }
        return valid;
    }

    private void simpanData() {
        if (validasiInput() == true) {
            try {
                String tgl = t_tanggalMasuk.getText();
                String kodmas = t_kodeBarangMasuk.getText();
                String nambar = t_namaBarangMasuk.getText();
                String satuan = t_satuan.getText();
                int jml = Integer.parseInt(t_jumlah.getText());
                String jenis = t_jenis.getText();

                // Buat objek Model_Barang
                Model_Barang barang = new Model_Barang();
                barang.setNama_barang(nambar);
                barang.setJenis_barang(jenis);
                barang.setSatuan(satuan);

                // Buat objek Model_Barangmasuk dan hubungkan Model_Barang dengan Model_Barangmasuk
                Model_Barangmasuk model = new Model_Barangmasuk();
                model.setTgl_masuk(tgl);
                model.setJumlah_masuk(jml);
                model.setKode_barangmasuk(kodmas);
                model.setMod_bar(barang);  // Pastikan properti mod_bar sudah diisi!

                // Simpan data ke layanan DAO dan tabel
                servis_psn.tambahData(model);
                tblModel_psn.tambahData(model);

                loadData();
                resetForm();
                tampilPanel();
                btn_tambah1.setText("TAMBAH");

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Input jumlah harus berupa angka.");
            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(this, "Pastikan semua input diisi dengan benar.");
            }
        }
    }

    

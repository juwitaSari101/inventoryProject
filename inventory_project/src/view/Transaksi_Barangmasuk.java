    private void hapusData() {
        int index = tbl_pesanan.getSelectedRow();
        if (index != -1) {
            Model_Barangmasuk brgmasuk = tblModel_psn.getData(tbl_pesanan.convertRowIndexToModel(index));
            if (JOptionPane.showConfirmDialog(null, "Yakin data akan dihapus", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                servis_psn.hapusData(brgmasuk);
                tblModel_psn.hapusData(index);
                loadData();
                resetForm();
            } else {
                JOptionPane.showMessageDialog(null, "Pilih dahulu data yang akan dihapus");
            }
        }
    }


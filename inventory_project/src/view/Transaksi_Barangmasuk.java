        mainPanel.repaint();
        mainPanel.revalidate();

        btn_tambah1.setText("SIMPAN");
        t_kodeBarangMasuk.setText(servis_psn.nomor());
        t_kodeBarangMasuk.setEnabled(false);

        java.time.LocalDate today = java.time.LocalDate.now();
